		public static uint[,] GetBrickArray(List<string> lines)
		{
			uint[,] brickArray = new uint[lines[0].Length, lines.Count];
			for (int i = 0; i < lines[0].Length; i++)
			{
				for (int j = 0; j < lines.Count; j++)
				{
					switch (lines[j][i])
					{
						case '.':
							brickArray[i, j] = 0;
							break;
						case '1':
							brickArray[i, j] = 1;
							break;
						case '2':
							brickArray[i, j] = 2;
							break;
						case '3':
							brickArray[i, j] = 3;
                            break;
                        case '4':
                            brickArray[i, j] = 4;
                            break;
                        case '5':
                            brickArray[i, j] = 5;
                            break;
						default:
							break;
					}
				}
			}
			return brickArray;
		}

